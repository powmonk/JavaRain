package com.danaldron.deeznuts;

class Particle                                                                                                                                            
{                                                                                                                                                         
	private int partX;                                                                                                                                
	private int partY;        
	private int partSway;
	
	public void setX(int x){                                                                                                                              
		partX = x;                                                                                                                                        
	}
	
	public void setY(int y){                                                                                                                           
		partY = y;                                                                                                                                        
	}                                                                                                                                                     

	public void setSway(int sway){                                                                                                                                    
		partSway = sway;                                                                                                                                     
	}                                                                                                                                                     
	
	public int getX(){                                                                                                                                    
		return partX;                                                                                                                                     
	}
	
	public int getY(){                                                                                                                                    
		return partY;                                                                                                                                     
	}                                                                                                                                                     
	                                                                                                                                                      
	public int getSway(){                                                                                                                                    
		return partSway;                                                                                                                                     
	}
	                                                                                                                                                      
}                                                                                                                                                         
                                                                                                                                                          
                                                                    
